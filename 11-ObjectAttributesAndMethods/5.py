class Music():
    def __init__(self, performer):
        self.performer = performer
    def __str__(self):
        return self.performer
    def __init__(self, song):
        self.song = song
    def __str__(self):
        return self.song
    def __init__(self, album):
        self.album = album
    def __str__(self):
        return self.album
    def __init__(self, year):
        self.year = year
    def __str__(self):
        return self.year
    